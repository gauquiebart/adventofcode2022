const parseInput = function (input) {
    const pairs = input.split('\n\n');
    return pairs
        .map(p => p.split('\n').map(l => eval(l)));
}

let logLevel = -1;
const log = function (str) {
    if(logLevel > 0) {
        console.log('--'.repeat(logLevel) + '-> ' + str);
    }
}

const packetsInRightOrderWithLogging = function(left, right) {
    logLevel = 0;
    const result = packetsInRightOrder(left, right);
    logLevel = -1;
    return result;
}

const packetsInRightOrder = function (left, right) {
    if(logLevel >= 0) {
        logLevel = logLevel + 1;
    }
    log(`Compare ${left} to ${right}`);
    if (Number.isInteger(left) && Number.isInteger(right)) {
        log(`both values are integers`);
        if(left === right) {
            log(`left is same as right --> continue`);
            return undefined;
        }
        if(left < right) {
            log(`left is lower than right --> inputs are in the right order`);
            return true;
        }
        log(`right is higher than left --> inputs are not in the right order`);
        return false;
    }
    if (Array.isArray(left) && Array.isArray(right)) {
        const [firstLeft, ...restOfLeft] = left;
        const [firstRight, ...restOfRight] = right;

        log(`both values are lists`);

        if(firstLeft === undefined && firstRight === undefined) {
            log(`both lists are same length, no comparison decision made`);
            return undefined;
        }

        if (firstLeft === undefined && firstRight !== undefined) {
            log(`left list runs out of items first, the inputs are in the right order`);
            return true;
        }

        const comparisonResultFirst = packetsInRightOrder(firstLeft, firstRight);
        if(comparisonResultFirst === undefined) {
            log('first left and first right are the same, continuing comparison on the rest of each');
            return packetsInRightOrder(restOfLeft, restOfRight);
        }

        log('comparison result determined stopping');
        return comparisonResultFirst;
    }
    if (Array.isArray(left) && Number.isInteger(right)) {
        log(`left array ${left} - right integer: ${right}`);
        return packetsInRightOrder(left, [right]);
    }
    if (Number.isInteger(left) && Array.isArray(right)) {
        log(`left number ${left} - right array: ${right}`);
        return packetsInRightOrder([left], right);
    }
    if (left !== undefined && right === undefined) {
        log(`left ${left} - right undefined - exhausted right -> the inputs are in the right order`);
        return false;
    }
    throw new Error("unhandled case");
}

const countRightOrderedPairs = function(pairs) {
    return pairs.map((p, index) => {
        const [left, right] = p;
        const inRightOrder = packetsInRightOrder(left, right);
        return inRightOrder ? index + 1 : 0;
    }).reduce((accumulator, currentValue) => accumulator + currentValue, 0);
}

test('both values are solely integers', () => {
    expect(packetsInRightOrder(4, 5)).toBeTruthy();
    expect(packetsInRightOrder(5, 4)).toBeFalsy();
    expect(packetsInRightOrder(4, 4)).toBeUndefined();
});

test('both values are solely integers in single element array', () => {
    expect(packetsInRightOrder([4], [5])).toBeTruthy();
    expect(packetsInRightOrder([5], [4])).toBeFalsy();
    expect(packetsInRightOrder([4], [4])).toBeUndefined();
});

test('both values are same length of list of integers', () => {
    expect(packetsInRightOrder([1, 1, 3, 1, 1], [1, 1, 5, 1, 1])).toBeTruthy();
    expect(packetsInRightOrder([1, 1, 5, 1, 1], [1, 1, 3, 1, 1])).toBeFalsy();
    expect(packetsInRightOrder([1, 1, 3, 1, 1], [1, 1, 3, 1, 1])).toBeUndefined();
});

test('both values are simple lists of different length', () => {
    expect(packetsInRightOrder([1, 1, 3], [1, 1, 3, 1])).toBeTruthy();
    expect(packetsInRightOrder([1, 1, 3, 1], [1, 1, 3])).toBeFalsy();
});

test('left is list, right is number', () => {
    expect(packetsInRightOrder([1], 2)).toBeTruthy();
    expect(packetsInRightOrder([2], 1)).toBeFalsy();
    expect(packetsInRightOrder([2], 2)).toBeUndefined();
    expect(packetsInRightOrder([2, 3, 4], 4)).toBeTruthy();
});

test('left is number, right is list', () => {
    expect(packetsInRightOrder(1, [2])).toBeTruthy();
    expect(packetsInRightOrder(2, [1])).toBeFalsy();
    expect(packetsInRightOrder(2, [2])).toBeUndefined();
});

test('nested arrays', () => {
    expect(packetsInRightOrder([9], [[8, 7, 6]])).toBeFalsy();
    expect(packetsInRightOrder([[4, 4], 4, 4], [[4, 4], 4, 4, 4])).toBeTruthy();
    expect(packetsInRightOrder([7, 7, 7, 7], [7, 7, 7])).toBeFalsy();
    expect(packetsInRightOrder([], [3])).toBeTruthy();
    expect(packetsInRightOrder([[[]]], [[]])).toBeFalsy();
    expect(packetsInRightOrder([1,[2,[3,[4,[5,6,7]]]],8,9], [1,[2,[3,[4,[5,6,0]]]],8,9])).toBeFalsy();
});

